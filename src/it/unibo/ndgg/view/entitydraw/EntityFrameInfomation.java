package it.unibo.ndgg.view.entitydraw;

/**
 * Represents the information about the frame in a sprite sheet.
 */
public interface EntityFrameInfomation {

    /**
     * Returns the height of the frame.
     * @return
     *        the height of the frame
     */
    int getFrameHeight();

    /**
     * Returns the width of the frame.
     * @return
     *        the width of the frame
     */
    int getFrameWidth();

}
