package it.unibo.ndgg.controller;

/**
 * An enumeration representing the type of input.
 */
public enum SimpleInput {

    /**
     * Represent a jump movement.
     */
    JUMP,
    /**
     * Represent a movement to the left.
     */
    LEFT,
    /**
     * Represent a movement to the right.
     */
    RIGHT,
    /**
     * Represent the throw of the sword.
     */
    THROW,
    /**
     * Represent the change of the guard.
     */
    CHANGE_GUARD,
    /**
     * Represent an attack.
     */
    ATTACK;


}
